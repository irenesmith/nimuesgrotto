					<h1>Review: Diabolique</h1>
					<p class="author">by Gene von Halo, Jr.</p>

          <p>When I was a boy, I saw a movie that scared me so bad, I wet my pants.</p>

          <p>Up to then, my movie experience consisted mostly of westerns and musicals. The only monster films I had seen were Creature from the Black Lagoon and Ed Wood's Bride of the Monster, both of which thrilled, but didn't frighten me. So I had no qualms when I got to the theater on a Friday night and found that the movie playing was called Diabolique. I had no idea what that even meant.</p>

          <p>I bought a ticket and bag of popcorn and found myself a seat. I had noticed, but paid little attention to, a sign at the ticket booth that said, &quot;Absolutely no one admitted during the last 10 minutes of the movie.&quot;</p>

          <p>In those days (the mid 1950s), people didn't necessarily arrive at the beginning of a film. There were no ushers to shoo everyone out at the end, so if you showed up late and missed the first part of a movie, you simply stayed and watched the first part during the next showing. A common saying was, &quot;This is where I came in.&quot;</p>

          <p>I got there at the beginning of the film and sat munching my popcorn, unaware of what lay in store.</p>

          <p>These days, Diabolique is available online, both the longer French version (Les Diaboliques) with English subtitles, and the US release, dubbed in English. Plus, there have been several Hollywood remakes over the years, so you may know what happens.</p>

          <p>Michel Delassalle, the principal of a boys boarding school in Paris, is a tyrant and a cruel, two-timing creep. He abuses his wife, a frail woman named Christina. And he treats his mistress, Nicole, a teacher at the school, just as badly. Also, he is abusive to the students.</p>

          <p>The wife and the mistress become friends, drawn together by a mutual hatred of Michel. Unable to take his cruelty any more, Nicole decides to murder him, and a reluctant Christina agrees to help. They lure Michel to an apartment a hundred or so miles away, drug him, and drown him in a bathtub.</p>

          <p>Imagine, if you will, a tender, young me watching this very adult plot and graphic murder scene. There were no movie ratings to protect kids like me, and though I'd seen many a cowboy gunned down, their deaths were quick and sanitary compared to Michel Delassalle's. His struggles as his wife and his mistress force his head under water and hold him there were gut wrenching to everyone in the theater, and especially to me.</p>

          <p>The two women put the dead man in a hamper, drive back to the school at night, and dump him into a murky, unkempt swimming pool. The idea being, when the body floats to the surface in a day or so, it will look like an accidental drowning.</p>

          <p>Except the body doesn't float to the surface. Instead, it disappears. At this point, the plot morphs from a murder story into a horror story, the likes of which I had never seen.</p>

          <p>Remember that sign about the last ten minutes of the movie? That's when I wet my pants.</p>

