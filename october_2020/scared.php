					<h1>Are you Scared Yet?</h1>
          <p class="author">by Matthew D. Smith</p>
          <p>I must confess, I made a terrible mistake. I don&rsquo;t know where to start, Barry King was my friend. When I was a child, he helped me get my career started. When he needed me most, I stood up for him. Like when the state of New Jersey put him on trial on bogus charges. Nancy Giovanni claimed that he abused her son Niko. It&rsquo;s a huge understatement to say that Barry was only a little eccentric. Weird yes, but not a child molester. Thanks to testimony from me and others who knew him well, he was cleared of all charges.</p>

          <p>In 2009, he died from a drug overdose. Four years later, I wasn&rsquo;t getting any work. I was desperate, so I started writing a book claiming that Barry molested me. I filled it with graphic sex scenes that would make even the toughest man puke. Now, if you showed this book to a die hard fan, they could destroy every lie with decades of evidence. Show the book to the average person, they&rsquo;ll eat it up like candy. It&rsquo;s so graphic, how could I make it up? Besides, Barry was dead, what could he do?</p>

          <p>Problem was, most people didn&rsquo;t care. No matter who I showed the book to, they threw it back at me. So I went a step further, I sued his estate. I figured it wouldn&rsquo;t work if I did it on my own, so I begged my friend Will Sellers for backup. Once we had our stories straight, we sued the estate multiple times to no avail.</p>

          <p>When all hope seemed lost, a British documentarian named Richard Nelson offered to make a documentary out of our story. Will and I polished our stories a bit more, and in 2017, he filmed the documentary &ldquo;The Dark Side of Barry King.&rdquo; Now the film is about to premiere at Sundance. Barry&rsquo;s fans protested loudly. I&rsquo;m confident that the Sundance audience will be more open to our bullshit.</p>

          <p>As I stared out my hotel window, I saw the protesters from six stories above. They were chanting the lyrics to &ldquo;Can&rsquo;t Keep Me Down,&rdquo; and holding up signs saying &ldquo;#justiceforbarry&rdquo; There were pictures of his face, mostly from early albums. I heard Richard say &ldquo;How are you feeling?&rdquo;</p>

          <p>I said, &ldquo;Nervous, you?&rdquo;</p>

          <p>&ldquo;I&rsquo;m fine. Just relax, nobody&rsquo;s going to attack you. You and Will are brave for telling your stories.&rdquo;</p>

          <p>&ldquo;Thank you, that means a lot.&rdquo;</p>

          <p>&ldquo;Trust me, when the networks air this, there will be more victims willing to tell the truth. Just like Cosby.&rdquo; I could have told him how wrong he was, but it would raise his suspicions. Hell, I was scared when I realized the story I told him didn&rsquo;t match my deposition. Good thing that was private, because Richard would have torn me apart. So, I didn&rsquo;t say anything and he left.</p>

          <p>The moment the door closed, I heard a familiar voice say &ldquo;Joseph Carson, you son of a bitch.&rdquo;</p>

          <p>I turned around. Barry King sat behind the counter. He didn&rsquo;t look a day over forty, surprising for a dead man. He wore an all white tuxedo with a white cowboy hat. He looked me right in the eye. I looked at his hat to avoid eye contact.</p>

          <p>&ldquo;Barry, how have things been?&rdquo; I said.</p>

          <p>&ldquo;Things have been rather shitty Joe. There I am, relaxing in Heaven, knowing my legacy was safe. At least, it was, until tonight. Someone decided to slander my good name for a paycheck.&rdquo;</p>

          <p>&ldquo;But Will and I aren&rsquo;t getting paid.&rdquo;</p>

          <p>&ldquo;Yes, and your little mockumentary doesn&rsquo;t mention your 100 million dollar lawsuit. So I guess lies are the new truth, huh Judas?&rdquo;</p>

          <p>&ldquo;Judas?&rdquo;</p>

          <p>&ldquo;That&rsquo;s your new nickname. I figured it&rsquo;s fitting, considering the situation. You betrayed me Judas. You said you would protect me, and you haven&rsquo;t. You&rsquo;ve failed Judas, and you will learn what happens when you fuck with the dead.&rdquo;</p>

          <p>&ldquo;What are you going to do, sue me?&rdquo;</p>

          <p>&ldquo;Oh no boy, I&rsquo;m gonna reveal the truth. Now, you could be a real man and tell the world the truth yourself. But if you don&rsquo;t, I&rsquo;ll do it for you. It&rsquo;s your choice Judas, choose wisely.&rdquo; Before I could respond, he disappeared.</p>

          <p>Will came in and said &ldquo;Joe, it&rsquo;s time.&rdquo; We went down to the auditorium, sitting in the front row. I couldn&rsquo;t help but think about that name, Judas. Why was that familiar?</p>

          <p>So I asked Richard &ldquo;Does the name Judas mean anything to you?&rdquo;</p>

          <p>&ldquo;Yes, there&rsquo;s the biblical figure named Judas Iscariot. He traded Jesus&rsquo; location to the Romans for money. Did somebody call you that? God, King&rsquo;s fans really are nuts.&rdquo;</p>

          <p>&ldquo;Actually, that&rsquo;s what Barry called me.&rdquo;</p>

          <p>&ldquo;How could he do that, he&rsquo;s dead?&rdquo;</p>

          <p>I couldn&rsquo;t tell him a ghost visited me, so I lied. &ldquo;During the trial, that&rsquo;s when he called me Judas.&rdquo;</p>

          <p>&ldquo;Odd. We&rsquo;ll add that in later.&rdquo; The lights dimmed, and the film started. Soon, I thought, the world was gonna be on my side. That was, until my deposition started playing. It then switched back and forth between the film and the deposition. Every lie was uncovered, and the audience started throwing stuff at Will and me. Even Richard started throwing stuff at us. Will and I ran to my van, and we fled the festival. As we left the city, Will said &ldquo;What the hell just happened? Why did the deposition play?&rdquo;</p>

          <p>&ldquo;It was Barry. He did it.&rdquo; I said.</p>

          <p>&ldquo;How? He&rsquo;s dead.&rdquo;</p>

          <p>&ldquo;He visited me tonight and warned me that he would reveal the truth. I thought he was bluffing.&rdquo;</p>

          <p>&ldquo;Jeez, that&rsquo;s insane.&rdquo;</p>

          <p>A voice from behind said &ldquo;So, I have a question. Are you scared yet?&rdquo;</p>

          <p>I looked, and I saw Barry in the back. He then grabbed the wheel and we drove off the bridge, crashing into the river. Barry disappeared, leaving Will and me to drown.</p>
