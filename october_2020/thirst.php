					<h1>Thirst</h1>
					<p class="author">by John Governale</p>
          <p>Because of the wind and rain, it took Emily a moment to realize someone was knocking on her apartment door. She forced her eyes away from her television and said, &quot;Who is it?&quot;</p>

          <p>She had to repeat the question, because an ill-timed clap of thunder masked her words.</p>

          <p>&quot;Hello,&quot; a man's voice said. &quot;It's George Santos. I live across the hall. Do you have a flashlight I can borrow?&quot;</p>

          <p>Emily undid the double lock, but left the safety chain hooked, opening the door a few inches.</p>

          <p>&quot;You're not a serial killer are you?&quot; she asked.</p>

          <p>&quot;No, ma'am,&quot; he said laughing. &quot;I'm just a man whose lights have gone out.&quot;</p>

          <p>Emily hesitated a moment, wondering how it was that her lights were on but his were out.</p>

          <p>She closed the door, undid the chain, then opened wide, inviting him in.</p>

          <p>The police were stumped in their investigation. Detective Harvey and Sergeant Clouse did a thorough search of the apartment, but found nothing amiss. It was if the occupant had simply vanished. They sent a couple of uniforms to the fourth floor to begin a canvas of the building.</p>

          <p>The two detectives knocked on the apartment across the hall from the one they had just searched. Emily spoke to the detectives through a three inch gap provided by the door chain.</p>

          <p>&quot;You're not a couple of serial killers are you?&quot;</p>

          <p>The detectives laughed.</p>

          <p>&quot;No, ma'am,&quot; Clouse said. &quot;We're just a couple of policeman trying to locate a Mr. George Santos. He lives across the hall from you. Do you remember the last time you saw him?&quot;</p>

          <p>&quot;Let me see your badges and IDs,&quot; she said.</p>

          <p>They held them up for inspection.</p>

          <p>Emily was quiet.</p>

          <p>&quot;Ma'am, if you wouldn't mind unchaining your door, it would be easier for us to talk.&quot;</p>

          <p>Still Emily was quiet.</p>

          <p>&quot;We won't come in. We'll stay right here. We just need to ask you a few questions.&quot;</p>

          <p>The door closed, and the detectives expected to hear the chain being removed, but instead heard the locks being engaged. After staring at the closed door for a minute, Clouse knocked on it.</p>

          <p>&quot;Go away,&quot; a woman's voice said from inside. &quot;I can't help you. I haven't seen him.&quot;</p>

          <p>&quot;Does she sound stressed to you?&quot; Harvey asked his partner.</p>

          <p>&quot;She does.&quot;</p>

          <p>Harvey sighed. &quot;I haven't busted open a door in two years. How stressed would you say she sounds? Under duress kind of stressed?&quot;</p>

          <p>Clouse nodded.</p>

          <p>&quot;I see a mound of paperwork in my future,&quot; Harvey said.</p>

          <p>&quot;Ma'am, this is Detective Bert Harvey of the city police. We need you to open your door. If you do not, we are authorized to force it open, and you will be responsible for the repairs.&quot;</p>

          <p>There was no response, so Harvey repeated the demand in a louder voice. When there was still no answer, he stepped aside and the beefier Clouse prepared to slam a size 13 shoe against the door.</p>

          <p>He took a breath and was about to kick when the door opened three inches and Emily peered out.</p>

          <p>&quot;What do you want?&quot; she snapped.</p>

          <p>&quot;Are you okay, ma'am?&quot;</p>

          <p>&quot;Of course I'm okay.&quot;</p>

          <p>&quot;You sound stressed.&quot;</p>

          <p>&quot;I AM stressed. You would be too if strange men threatened to kick in your door.</p>

          <p>&quot;We need to look in your apartment.&quot;</p>

          <p>&quot;Why?&quot;</p>

          <p>&quot;Open your door or we're going to force it open.&quot;</p>

          <p>&quot;Okay,&quot; Emily said exasperated. &quot;Okay.&quot;</p>

          <p>The door closed, but there was no sound of the chain being removed.</p>

          <p>Harvey looked at Clouse, and they both pulled their guns.</p>

          <p>&quot;Ma'am, stand back from your door.&quot; Harvey said.</p>

          <p>Clouse turned the knob and rammed his shoulder into the door, snapping the chain.</p>

          <p>They found George Santos tied to a chair. He was pale, but alive.</p>

          <p>&quot;Mr. Santos, we're police detectives. You're okay.&quot;</p>

          <p>Santos didn't look okay. He was conscious, but barely.</p>

          <p>&quot;Stay with him,&quot; Harvey said and went in search of Emily. All he found was an open window in the bedroom.</p>

          <p>&quot;You did what?&quot; Ravineas said.</p>

          <p>&quot;He knocked on my door and I was so thirsty,&quot; Emily said.</p>

          <p>&quot;And the police now have his body?&quot;</p>

          <p>&quot;He wasn't dead.&quot;</p>

          <p>Ravineas slapped his forehead.</p>

          <p>&quot;I should throw you into the sunshine. Do you have any idea what you've done?&quot;</p>

          <p>&quot;I was so thirsty,&quot; Emily said, a note of pleading in her voice.</p>

          <p>Ravineas stood silent for a long moment.</p>

          <p>&quot;Buenos Aires,&quot; he said.</p>

          <p>&quot;Buenos Aires,&quot; Emily said. &quot;Are there soap operas there?&quot;</p>

          <p>Ravineas growled, his canines showing.</p>

          <p>&quot;You will go to Buenos Aires. You will stay there. I will clean up this mess. But this is the second incident in less than 200 years. If there is ever a third, I will personally leave you on a beach where all the sunblock in the world won't save you.&quot;</p>

