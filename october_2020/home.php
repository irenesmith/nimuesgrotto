					<h1>Home Again</h1>
					<p class="author">by Britney Cole</p>
          <p>I read once that memories change over time. We tell and re-tell the stories of our lives until they become something quite different from what actually happened. This is not one of those stories, I promise. I can close my eyes today, nearly sixty years later, and still see the events of the story as though it had happened yesterday.</p>

          <p>I had two best friends when I was thirteen. Carla Rivera and Annie Boucher. I am an only child and Annie had an older brother, but Carla had hit the sibling jackpot. She was the youngest of five. She had four older brothers. Her mother was a quiet, mousy woman who seemed nice. She barely ever talked and when she did, it was about Carla&rsquo;s father.</p>

          <p>&quot;Hey Carla, we're going down to Luigi's to hang around, eat pizza, and look at the boys. Wanna come?&quot;</p>

          <p>&quot;Can&rsquo;t do it.&rdquo; she said. &ldquo;Ma says I have to go right home from school so I'll be there when Daddy comes back.&quot;</p>

          <p>She hopped on her bicycle and it wasn't until she was almost a block away that it hit me. Carla's father was dead. He'd been dead for over six months. He wasn't coming home&mdash;ever.</p>

          <p>I know it seems weird that I should forget such a thing, but Carla&rsquo;s dad had never been a big part of our lives. He worked long hours and wasn&rsquo;t often around. In fact, I couldn&rsquo;t remember a time when he had gone anywhere with all of us.</p>

          <p>We went to the Rivera house often because her parents both worked. We hung out in the basement and talked and smoked without having to worry about someone catching us. Carla had four older brothers, but they weren&rsquo;t around often either.</p>

          <p>&quot;What's the matter, goose walk over your grave?&quot; Annie Boucher was the third musketeer. She and Carla Rivera, and I had been friends since kindergarten. Wherever you saw one of us, the other two were likely to be nearby. That is, until Carla's father died in a car crash on the way home from the office Christmas party. Now Carla stayed home when she wasn't in school. I'd thought it was from grief.</p>

          <p>&quot;It's nothing,&quot; I muttered. &quot;Carla's mother has gone off the deep end.&quot;</p>

          <p>&quot;What do you mean?&quot;</p>

          <p>&quot;Carla's going home to wait for her dead father because her mother's expecting him.&quot;</p>

          <p>&quot;Creepy.&quot;</p>

          <p>We stopped at my house first. The thermometer hovered around 95 degrees and I wanted to change into a pair of shorts. By the time I changed, and we were walking down King Street, Annie was nagging at me to stop at Carla's house on the way.</p>

          <p>&quot;We've got to stop and get her, Suze. It's just not good for her to sit in the house all the time.&quot; She waited a few seconds and then, added, &quot;Besides, if her mother is that crazy, we've got to get her away from there.&quot;</p>

          <p>Finally, I gave in. We had to pass Carla's house anyway, so why not stop and try to get her to go with us?</p>

          <p>&quot;I guess you're right,&quot; I said. &quot;What harm can it do?&quot;</p>

          <p>Mr. Rivera's car was parked in the driveway.</p>

          <p>&quot;What a mess,&quot; said Annie. &quot;Nobody comes back from a mess like that.&quot;</p>

          <p>&quot;How do you suppose it got here?&rdquo; I asked. &ldquo;It wasn&rsquo;t there yesterday and it sure doesn't look like it goes on its own.&quot;</p>

          <p>The car looked as though a giant had crumpled it into a ball and tossed aside. The windshield had a huge hole through it and both doors were sprung from the frame. When we got closer, we saw that the back seat had popped out and had been thrown forward halfway into the front seat. The front seat and the dashboard were covered with dried blood. I had the strange feeling that the car was alive and hungry. I thought of Stephen King's Christine and shuddered.</p>

          <p>I knocked on the door. &quot;Hey, Annie,&quot; I whispered, &quot;when Carla opens the door, let's say, 'Hi--'&quot; But Annie was ahead of me, as usual.</p>

          <p>As the door swung open slowly, Annie shouted out, &quot;Hi honey, I'm home!&quot;</p>

          <p>Have you ever seen a corpse after it's been in the ground for six months? I wanted to run but I couldn't move. Even the pain in my arm where Annie grabbed me and dug her fingernails deep into the flesh had no power to break the inertia.</p>

          <p>Mr. Rivera's cheeks looked as though they had been dabbed with gobs of shortening. Great chunks of hair were missing and the scalp beneath was tattered. His nose was gone and his upper lip looked like it had been chewed; I didn't dare think about what might have done it. The moldy black suit he wore must have been the one in which he was buried. He turned and looked back over his should into the living room beyond and made a strange gargling noise, saying something that I couldn't understand.</p>

          <p>Carla appeared at her father's side. She had a happy smile on her face. Grasping the gruesome arm of the animated corpse, she said, &quot;Daddy's home.&quot;</p>
