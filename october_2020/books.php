					<h1>For the Love of Books</h1>
					<p class="author">by Pauline Wilbur</p>
          <p>I have lived in Milford all my life. It is a small town about two hours northwest of New York City. One of my favorite things about it is the library. Corny, I know, but it has long been my favorite place to spend time. Our library has a lot going for it from the children&rsquo;s room upstairs to the perpetual used book sale in the basement.</p>

          <p>My mother worked at the library when she was a high school student and when I got to high school myself, so did I. But the story I want to tell happened much earlier than that. Geeky as it sounds, one of my favorite activities when I was growing up was the Summer Reading Program. Every year you got a booklet to write about each of the books you read and there was always a great mixture of books to read. I was always the first to sign up. The last year that I participated was the best, and it wasn&rsquo;t entirely because of the books. It was because of&mdash;well, let me start at the beginning; I don&rsquo;t want to get too far ahead of myself.</p>

          <p>The children&rsquo;s books were downstairs when I was a kid, connected to the main area of the library where adults checked out books by a doorway with no door. There were two sections to the children&rsquo;s room, the main room and a much smaller room in the back where there was a doll house, Lincoln Logs to build with, and one bookcase completely filled with biographies.</p>

          <p>The rules for the reading program dictated that you had to read a certain number of several types of books, and I was looking for a biography. I had read most of the biographies and was having trouble picking out a new one. I stood there, running one finger alone the spines of the books, when I heard a voice say, &ldquo;Try the one about Clara Barton.&rdquo;</p>

          <p>&ldquo;Thanks.&rdquo; I turned around but my smile faded when I realized that I was alone.</p>

          <p>The voice belonged to a woman and sounded vaguely familiar. I looked out into the main room but it was also empty. I found the Children&rsquo;s Librarian at the main desk. When she came back to the children&rsquo;s room to check out my books, I expected her to comment on the Clara Barton book but she didn&rsquo;t say a word.</p>

          <p>&ldquo;I got the Cara Barton book like you suggested,&rdquo; I prompted her.</p>

          <p>&ldquo;I didn&rsquo;t suggest any books to you dear,&rdquo; said the woman.</p>

          <p>As soon as I heard her voice, I knew she hadn&rsquo;t. Her voice was nothing like the one I had heard. I went home with my books and didn&rsquo;t think much about it until two weeks later when I had to go back at the library. It was a hot day but the library was cool and quiet. I dropped my returns on the counter and strolled along the stacks, thumbing through the books trying to find something new and interesting.</p>

          <p>&ldquo;Have you read A Wrinkle in Time?&rdquo; asked a familiar voice.</p>

          <p>This time when I turned around, I saw a woman I hadn&rsquo;t seen at the library before. She was short and a bit on the plump side. She wore a simple dress and low-heeled white sandals. She made me think of the women in movies like &ldquo;It&rsquo;s a Wonderful Life&rdquo; and &ldquo;Mr. Smith goes to Washington.&rdquo; Of course, back in the 60s when this happened, people still dressed up to go to work so it didn&rsquo;t occur to me that it was strange in any way.</p>

          <p>&ldquo;Of course I read it. Why?&rdquo;</p>

          <p>Hers was clearly the voice I had heard on my last visit. She frowned and I thought I was about to get a lecture for making too much noise. Then she smiled at me, and I liked her immediately. &ldquo;Well,&rdquo; she said, &ldquo;if you liked that book, you&rsquo;ll love this one too.&rdquo; She pointed out a book called, A Wind in the Door.</p>

          <p>I picked it up and saw that it was a sequel to A Wrinkle in Time. I opened it and read the description.</p>

          <p>&ldquo;Thanks,&rdquo; I said and tucked it under my arm so I could pick out a few more books.</p>

          <p>She must have wandered away while I was looking because when I took my books to the check out counter, she was gone.</p>

          <p>While I was waiting to have my books stamped, I noticed that there were pictures of several women on the wall behind the desk.</p>

          <p>&ldquo;That&rsquo;s the woman who has been suggesting books for me,&rdquo; I said.</p>

          <p>&ldquo;Mrs. Gardner? Impossible. She was the librarian here over thirty years ago. She passed away in the stacks,&rdquo; she pointed to the back room, at the bookcase where they stored the biographies, and finished, &ldquo;right over there.&rdquo;</p>
