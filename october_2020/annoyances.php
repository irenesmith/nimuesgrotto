					<h1>Annoyances</h1>
					<p class="author">by John Governale</p>
          <p>
            There are seven things that annoy me. Well, there are more than
            seven, but right now seven in particular have risen to the top.
          </p>
          <p>
            One is a guy who, for no reason, looks me in the eye and smiles.
            Like that bozo with a newspaper. What's he doing reading a
            newspaper, anyway. He's too young to be reading a paper. He should
            be fiddling with his phone.
          </p>
          <p>
            Still, it annoys me that our eyes accidentally met, and instead of
            glancing away like a gentleman -- Oops. Sorry. Didn't mean to make
            eye contact. -- he looked right at me and smiled.
          </p>
          <p>
            If I were in a better mood, I might have smiled back. Not an
            engaging smile, just a quick polite smile of acknowledgement and
            then focus my attention back on my magazine.
          </p>
          <p>
            Another thing that annoys me is when my phone dies, knowing I'm
            depending on it for the commute home. I should invest in one of
            those external phone battery charging thingies for times like this.
          </p>
          <p>
            Fortunately, there was a magazine, some sort of silly movie star
            fan magazine, on the seat. Which is the third thing that really
            annoys me.
          </p>
          <p>
            People leave newspapers and magazines behind on the bus. But never
            good ones. What do they think -- that public transportation is a
            free drop zone for inferior reading materials?
          </p>
          <p>
            I wonder if that guy is reading a paper because his phone died. He
            should get one of those external charging thingies. And he should
            keep his smiles to himself.
          </p>
          <p>
            I'm in a cranky mood. That's the fourth -- or is it the fifth? --
            thing that really annoys me: being in a cranky mood, especially
            when some cute paper-reading guy smiles at me.
          </p>
          <p>
            He's not looking at me now. Not having another go at a smile. He's
            reading his newspaper. Or whosever newspaper it is.
          </p>
          <p>
            Another huge annoyance is when I'm staring at someone, but I'm not
            really staring at them. I'm lost in thought and my eyes are
            focused inward, even though they appear to be aimed at someone.
          </p>
          <p>
            Well, at least this time he didn't smile. He just glanced up, met
            my eyes, and ... well, maybe he smiled. I looked down at my
            magazine too quick to see.
          </p>
          <p>
            In my head, a man's voice said, "You need to get off the bus."
          </p>
          <p>
            I didn't know that a man's voice speaking in my head annoyed me
            until it just happened.
          </p>
          <p>
            "You get off the bus," I said silently to the voice in my head.
          </p>
          <p>
            "Look up," the voice said.
          </p>
          <p>
            The guy with the paper was looking at me. He widened his eyes
            slightly and gave his head a single nod toward the exit.
          </p>
          <p>
            "Get off the bus," the voice in my head said.
          </p>
          <p>
            I have this reaction to things that annoy me. It's called
            contrariness.
          </p>
          <p>
            "No," I said in my head. "I'm not getting off the bus."
          </p>
          <p>
            The man sighed and put down his newspaper.
          </p>
          <p>
            If he comes over here, I'm going to punch him in the throat.
          </p>
          <p>
            "You are not going to punch me in the throat. Everyone on this bus
            is going to be dead in less than two minutes. If you punch me, you
            and I will be dead too."
          </p>
          <p>
            He pulled the request-to-stop cable, got up, and walked to the back
            exit door, which was right in front of me.
          </p>
          <p>
            I doubled up my fist.
          </p>
          <p>
            He looked at me, and a voice in my head said, "Don't let your
            contrariness get you killed. Being dead is annoying."
          </p>
          <p>
            The door opened and he got off. At the last possible second, I
            hopped up and got off, too, leaving my magazine behind.
          </p>
          <p>
            The bus pulled away. I looked at Mr. Speak-in-my-head. "This isn't
            a very safe neighborhood," I said out loud.
          </p>
          <p>
            "It's safer than being on that bus."
          </p>
          <p>
            I turned and looked at the bus, which was now a block away. I
            started to say something when a hand covered my mouth and nose and
            an arm circled my waist, picking me up.
          </p>
          <p>
            The sixth and seventh things that annoy me, royally annoy me, are
            being rendered unconscious and being kidnapped.
          </p>
          <p>
            I awoke in a chair, a recliner of some sort. To my relief, I was
            not tied up. I put the chair in an upright position and found my
            kidnapper sitting opposite me. Before I could speak, he pointed a
            finger at a TV, which came on.
          </p>
          <p>
            "Tragedy this evening," a newscaster said. "A commuter bus was
            struck by a runaway fuel truck, which burst into flames on impact.
            Neither the truck driver nor anyone on the bus is believed to have
            survived."
          </p>
          <p>
            Another finger point and the TV turned off.
          </p>
          <p>
            "Who are you, how did you know the bus was going to crash, and why
            did you kidnap me?"
          </p>
          <p>
            "I didn't kidnap you, I saved you."
          </p>
          <p>
            "Why?"
          </p>
          <p>
            "Because you're being recruited."
          </p>
          <p>
            "Recruited for what?"
          </p>
          <p>
            "If I tell you now, you'll just get annoyed."
          </p>
          <p>
            "I'm already annoyed."
          </p>
          <p>
            I gave him a hard stare and waited.
          </p>
          <p>
            "The Galactic Protection Force for the Preservation of Earth."
          </p>
          <p>
            "The what?"
          </p>
          <p>
            "The Galactic Protection Force for the Preservation of Earth."
          </p>
          <p>
            It annoys me when I'm recruited into things when I don't know what
            are. And it annoys me even more when people predict what is going
            to annoy me.
          </p>
          <p>
            Though my recruiter was three feet away, he instinctively put a
            hand over his throat.</p>
