					<h1>Stinkweek</h1>
          <p class="author">by Dennis Rudder</p>
          <p>Once upon a time there were three bears -- a papa bear, a mama bear, and a baby bear.</p>

          <p>&quot;Why,&quot; baby bear asked one day, &quot;don't we eat people? They look very tender.&quot;</p>

          <p>&quot;Because we don't,&quot; papa bear said and looked at mama bear for support.</p>

          <p>&quot;Because we don't,&quot; mama bear said. &quot;Now eat your berries and grubs like a good little baby bear.&quot;</p>

          <p>Baby bear ate his berries and grubs, but thought about a small human he'd seen in the woods, and his supper tasted bland.</p>

          <p>Later that night after baby bear had gone to sleep, mama and papa bear discussed their son's appetite.</p>

          <p>&quot;Wherever did he get the idea of eating people?&quot; mama bear said.</p>

          <p>&quot;Not from me,&quot; papa bear said, his eyes large and innocent.</p>

          <p>&quot;Well, certainly not from me,&quot; mama bear said. &quot;Maybe we need to expand his diet. Maybe he's not getting all the nutrition he needs from berries and grubs.&quot;</p>

          <p>&quot;Expand it how? papa bear asked.</p>

          <p>&quot;I don't know. Maybe add an occasional squirrel or rabbit.&quot;</p>

          <p>&quot;Those are hard to catch.&quot;</p>

          <p>&quot;Then what do you suggest?&quot;</p>

          <p>&quot;Maybe if we fed him a small human, he'd see how unappetizing they are.&quot;</p>

          <p>&quot;But what if he likes it?&quot;</p>

          <p>The two sat in silence, listening to baby bear's deep, sleep-filled breathing.</p>

          <p>&quot;What if we rubbed the human with stinkweed? That would make it taste disgusting,&quot; papa bear said.</p>

          <p>Mama bear nodded. &quot;But where are we going to find a small human?&quot;</p>

          <p>&quot;God will provide,&quot; papa bear said solemnly.</p>

          <p>&quot;Goldy Locks, if I've told you once, I've told you a thousand times, don't go into the woods alone.&quot;</p>

          <p>&quot;Why?&quot;</p>

          <p>&quot;Because there might be bears.&quot;</p>

          <p>&quot;But, Grandma, bears don't eat people, they eat berries and grubs.&quot;</p>

          <p>Grandma Locks pressed her lips together and shook her head. &quot;How do you even know that?&quot;</p>

          <p>&quot;I read it in a book.&quot;</p>

          <p>&quot;Hummmph. Read it in a book. If a bear grabs you and eats you, don't come running to me.&quot;</p>

          <p>And so it was that the very next day, Goldy Locks went into the woods alone. She had in her backpack a peanut butter and honey sandwich, a thermos of milk, and a copy of Wilson's Woodlore.</p>

          <p>&quot;I see her,&quot; papa bear said quietly. &quot;You keep baby bear busy. I'll catch her and rub her with stinkweed.&quot;</p>

          <p>Goldy saw a cluster of pink flowers. Wilson's Woodlore would tell her what they are. As she removed her backpack, she was snatched up by a large bear.</p>

          <p>&quot;What are you doing?&quot; Goldy Locks yelled.</p>

          <p>The bear, of course, could not answer because bears and people speak entirely different languages.</p>

          <p>The bear held her down with one enormous paw and began to rub her head to toe in stinkweed. As he did he yelled, &quot;Mama bear, baby bear, come see what I've caught.&quot;</p>

          <p>&quot;You don't eat people. You don't eat people,&quot; Goldy Locks said as best she could with a bear holding her down and stinkweed in her mouth. &quot;You don't eat people. It says so in a book.&quot;</p>

          <p>Baby bear was as excited as his parents had ever seen him.</p>

          <p>&quot;You caught a human. You caught a human,&quot; he said and spun around with glee.</p>

          <p>&quot;Yes, son,&quot; papa bear said. &quot;I caught it for you. It's yours to eat.&quot;</p>

          <p>Baby bear could hardly believe his good fortune. This was the best day ever.</p>

          <p>At that moment, two hunters had the bears in their sights. One touched the other on the arm. &quot;Isn't that that snotty little Locks girl?&quot;</p>

          <p>&quot;It is. But we can't let the bears eat her.&quot;</p>

          <p>&quot;They're not going to eat her. I can smell her from here. She's covered in stinkweed.&quot;</p>

          <p>A moment later, the hunter saw how wrong he was.</p>

          <p>Papa bear lost a sizable chunk of his brain. And later, his hide, which was made into a rug.</p>

          <p>Grandma Locks lost a granddaughter.</p>

          <p>&quot;Serves the little know-it-all right,&quot; she said.</p>

          <p>Mama bear lost a mate and regretted she'd ever agreed to the stupid scheme.</p>

          <p>And baby bear lost his appetite for humans. &quot;They taste like stinkweed,&quot; he said.</p>

          <p>The following year, a hiker found a backpack. Inside was the moldy remains of a sandwich, a thermos of sour milk, and a copy of Wilson's Woodlore, which she used to identify some nearby flowers. They were pink nasturtiums.</p>

          <p>Baby bear, who had grown considerably, eyed the hiker and wondered if a stinkweed after-taste was really such a bad thing.</p>
