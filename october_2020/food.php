					<h1>Food for Thought</h1>
          <p class="author">by Irene P. Smith</p>
          <p>&quot;No more junk food,&quot; said Alice. She took a deep breath. &quot;No more than one thousand calories a day. More salads, no cookies. Or cake,&quot; she said.</p>

          <p>The first week went well. She stayed within her calorie allotment and, when she weighed herself on Sunday morning, she felt a thrill of excitement at the results.</p>

          <p>She leaned over her husband&rsquo;s shoulder as she refilled his coffee. &quot;I&rsquo;ve lost five pounds.&quot;</p>

          <p>&quot;I knew you could!&quot; He turned in his seat to pull her close. Coffee flavored kisses reinforced her pride.</p>

          <p>Her son put down his cell phone; a rare occasion. &quot;Way to go, Mom!&quot;</p>

          <p>&quot;But what about the party?&quot; her husband asked. &quot;Maybe we should cancel.&quot;</p>

          <p>&quot;I&rsquo;ll manage,&quot; Alice said.</p>

          <p>That evening at her husband&rsquo;s birthday party, Alice was as good as her word. Even though the sweet fragrance of the cake teased her senses she didn&rsquo;t even taste the frosting.</p>

          <p>The second week Alice wrestled with temptation. Thursday morning Alice poured herself an extra cup of tea, hoping to stave off the hunger pangs. Every time she thought about eating, she took a sip and reminded herself how much better she felt already.</p>

          <p>&quot;Don't you want a piece of cake?&quot;</p>

          <p>&quot;Who said that?&quot;</p>

          <p>&quot;Who do you suppose?&quot;</p>

          <p>She lifted the lid off the pedestaled plate that held the leftover birthday cake. &quot;I must be hearing things.&quot;</p>

          <p>She was about to replace the lid when the cake said, &quot;I knew you wouldn't forget me.&quot;</p>

          <p>&quot;Cakes don't talk,&quot; said Alice.</p>

          <p>&quot;Who says we don't? I&rsquo;m more than just a delectable confection, you know.&quot;</p>

          <p>&quot;Yeah.&quot; The sound came from the kitchen. &quot;We're here to be eaten.&quot;</p>

          <p>Alice followed the voice to a huge economy-sized bag of chips. Her mouth watered. She could taste the saltiness of the chips on her tongue. She reached for the bag, not sure whether she intended to put it away or open it and shove a handful of chips into her mouth.</p>

          <p>&quot;Yeah, come on. Eat a few.&quot; The voice was masculine; as deep and sexy as Barry White. &quot;You know you want to. Just one ...&quot;</p>

          <p>&quot;Eat me instead. Fewer calories, you know,&quot; said one of the oranges in the large fruit bowl on the kitchen table. Its voice was high and squeaky. It sounded as though it was speaking through its nose.</p>

          <p>&quot;That wouldn't be so bad.&quot; Alice turned away from the chips and reached for the orange.</p>

          <p>&quot;I knew you would do the right thing.&quot; said a carrot. It sounded so self-righteous that it repulsed her. She turned back to the chips. &quot;But how can I eat something that talks to me?&quot;</p>

          <p>&quot;That's what we're here for,&quot; the chips replied.</p>

          <p>Straightening her back, she returned to the dining room and gathered up the dirty dishes. Ignoring the renewed pleas from the cake, she loaded the dishwasher and left the room. No, she admitted to herself. I ran away.</p>

          <p>On the way to the grocery store she tried to convince herself that she imagined the whole thing. &quot;Food can&rsquo;t talk.&quot;</p>

          <p>It was nearly lunchtime and the store was deserted. Only a handful of people pushed their carts up and down the aisles.</p>

          <p>The only sound was the bland music playing through the PA system, occasionally interrupted by a request for a price check. Then she turned into the candy aisle. She was looking for something sugar free but sweet enough to help her stay on track when she heard, &quot;Come on, baby, give me a try.&quot;</p>

          <p>A bag of candy bars lifted itself into an upright position. The contents moved slowly as it spoke again. &quot;You won&rsquo;t be sorry.&quot;</p>

          <p>It was becoming natural to respond but what if someone saw her talking to a bag of candy bars? She abandoned her cart and ran for the car.</p>

          <p>Once behind the wheel, she leaned forward and moaned softly. &quot;I'm losing it!&quot;</p>

          <p>&quot;I can help.&quot; This voice was female; soothing and strangely familiar.</p>

          <p>She looked around. &quot;Who are you?&quot;</p>

          <p>&quot;You need my help.&quot;</p>

          <p>&quot;I guess?&quot;</p>

          <p>&quot;Divide and conquer,&quot; said the voice. A pause, and then, &quot;I&rsquo;ll show you what I mean when you get home.&quot;</p>

          <p>Once she was home she went to the kitchen. &quot;Okay, now what?&quot;</p>

          <p>&quot;All your life you have had a confrontational attitude about food. You always gave in to it. What you need to do is divide and conquer,&quot; the voice said.. &quot;When you have a mile-long list of things to do, how do you get them done?&quot;</p>

          <p>&quot;That&rsquo;s easy enough. My mother always told me to...&quot; Alice trailed off, an idea beginning to form in her mind.</p>

          <p>&quot;Told you what?&quot; prompted the voice.</p>

          <p>&quot;Told me to leap one hurdle at a time.&quot;</p>

          <p>&quot;What&rsquo;s your favorite dessert?&quot;</p>

          <p>&quot;Ice cream,&quot; said Alice without hesitation. &quot;Peppermint ice cream.&quot;</p>

          <p>&quot;I suppose you have some?&quot;</p>

          <p>Alice opened the freezer. She stared at the peppermint ice cream. All the while she struggled to ignore the enticing suggestions from the food around her. &quot;What now?&quot;</p>

          <p>&quot;Allow yourself one spoonful,&quot; said the voice.</p>

          <p>&quot;What will that prove? And who are you?&quot;</p>

          <p>&quot;I thought you would have figured that out by now. I'm your willpower&mdash;you know, that small quiet voice in your head?&quot; After a pause, the voice continued, &quot;When you need to resist temptation, give in a little bit.&quot; Before she could say anything, the voice continued, &quot;A big bowl of ice cream is bad, but a small bowl?&quot;</p>

          <p>&quot;Not so bad.&quot;</p>

          <p>&quot;Good work, Lissie! You're catching on.&quot;</p>

          <p>Lissie? And with that, Alice realized whose voice she was hearing. Her mother might be long gone, but every word from the voice sounded like her mother; her most ardent supporter.</p>

          <p>&quot;And the other voices? The potato chips, the cake?&quot;</p>

          <p>&quot;Your inner demons. They come from your imagination. After all, cake can't talk.&quot;</p>

          <p>Alice savored the cool sweetness of that one spoonful of ice cream. Feeling strangely satisfied, she went about her day with renewed determination.</p>				</article>
