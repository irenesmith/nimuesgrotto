					<h1>They Understood</h1>
					<p class="author">by Elizabeth Sullivan</p>
					<p class="lead">The moment his lips left hers she knew she needed to see him again. Knew it couldn't be goodbye forever. He claimed it would be a final time, so definitive he is. But as he took a step away from her, she could feel the cold drift in.</p>

					<p>She watched his beautiful face stare at her. His chocolate eyes, tucked underneath his dark hair, both complimented each other deeply. His face, showing just the smallest hint of facial hair, indicating the fact that they had stayed up the last two days. Though it was technically two days, she thought, since just yesterday they had just popped over to the year 1969. He had done that for her, she realized now. He had always done for her, she knew.</p>
					
					<p>His tall, angular frame was hunched slightly as he walked back to his 'machine' as he always called it. The powerful little bugger that got him where he needed to be when he needed to be there. Her heart skipped a beat when he stopped walking. Her memory of the day before so vivid.</p>

					<p>They had ended up right at the start of the festival, and she couldn't put to words what it was like to hear those sounds, so real, so vivid. They had dressed to fit in, looked just like the other 20 somethings that were ecstatic to be there. Her heart was soaring, and she knew he knew it. He grabbed her hand and pulled her through the crowd, careful as not to disturb anything.</p>

					<p>She longed to feel his hand in hers now. It fit so well, she recalled, and she felt safe and comfortable there. In his arms. Like she had been...just hours before. Her vision started to blur as she felt the tears. She promised him she would let him go, he told her there would be a time and it needed to happen. She always assumed it was a mythical time they talked about, but a time that never truly happens...</p>

					<p>Here it was, she reminded herself, the moment that he would get into his machine and leave her. Forever</p>

					<p>She started to panic when he appeared to stand, frozen in front of her. He was only 7-8 feet away, but she felt like he was gone already. Her heart raced. What would love be like without him? She's known--loved him most of her adult life, how can she just go on without him. Why should she have to? He's always found a way to explain his disappearances. Her family understood. As a matter of fact, her father adored him. </p>

					<p>He moved, but it was towards his machine and away from her. She bit her lip trying to hold back tears. She had promised him, just last night...and again this morning, that she wouldn't cry. It was proving to be more difficult than she had thought. Well, of course it would. Last night she lay in his arms, protected from the feeling of loneliness. Here, as she stood in the woods just 100 feet behind her house, she wasn't protected. </p>

					<p>He stopped again, and her breath caught as he turned to look at her. His eyes were brimming red and she couldn't stop the tears from rolling down her cheek now. The sobs stayed hidden in her throat, but the tears were rolling. He raised a hand to run through his hair, a nervous habit of his. He looked over his shoulder to his machine and quickly back to her. They stood like this for nearly forty-five seconds before he opened his mouth, but nothing came out. He was only a foot away from the machine, and nearly 10 from her. She felt her breathing pick up as he stared at her.</p>

					<p>"They expect me back," he whispered. If it was anyone else, they wouldnt have heard him, but she wasnt just anyone else. </p>

					<p>"I know," she choked out. He nodded and turned back to the machine, reaching his hand out to the door. </p>

					<p>She promised herself she'd watch the machine disappear, and then drag herself to the comforts of her cottage in the woods. He stepped into the machine and shut the door. The tears rolled faster down her cheek as she saw the lights start up. She knew, as based in the other times that they've stood in this exact spot, that the machine  lit up and would disappear in about 3 minutes. She felt the machine pick up and knew it would disappear from sight, and this would be the last time. </p>

					<p>She tried to memorize the red color of the lights, and she thought of the small set up he had inside. It was so homey, well at least it was since he allowed her to redecorate. She felt a small bubble of happiness at the reminder of how they were together. She was head over heels in love with him, and in that moment she realized she would never find anyone like him. </p>

					<p>She watched the machine as it started to faintly squeal, which is what happened right before it left. But suddenlyâ€‹ it stopped. The light turned off and her heart raced, palms were sweating as the door opened and he raced to her. She was in his arms before she could even think. He lifted her feet off the ground and was holding to her so tight she thought that her bones would crush. She didn't care, she decided as he smothered her in kisses. </p>

					<p>"You're everything to me, Aurora, I can't leave you."</p>

					<p>"What about your family, Leo?" she asked as he pulled away from her. He threw a glance back to where the machine once stood. </p>

					<p>"They already called Lennon, he was next in line," he whispered, looking back at Aurora. He wiped away her tears. </p>

					<p>"I'm sorry," she whispered, as they walked back toward her house. </p>

					<p>"I'm not," he said squeezing her hand, "they understood."</p>

