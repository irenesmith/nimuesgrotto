					<h1>Twelve Months</h1>
					<p class="author">by John Governale</p>
					<p>Margery eyed the half bag of chips and tapped her nails on the table.</p>

					<p>The chips ignored her, pretending to read the label on a nearby box of cookies. It spent a little too long at this.</p>

					<p>"Cut. Reset."</p>

					<p>A woman rushed over and dabbed at Margery's make up.</p>

					<p>Ed, the puppeteer, crawled out from under the table.</p>

					<p>"Sorry," he said. "Quick bathroom break?"</p>

					<p>"Okay," the director answered. "Everybody, take ten."</p>

					<p>Margery stayed put. As she waited, she tapped her nails on the table.</p>

					<p>"Careful with your nails," the AD said.</p>

					<p>Margery put her hands in her lap.</p>

					<p>A year ago, Margery—whose real name was Alice Stall—had been eaten by an alien in a blockbuster space movie. She'd had 14 lines, plus a death scream. They'd kept all of her lines, but used a voice talent to dub the scream.</p>

					<p>That was to have been her springboard role, the one that would land her steady work—not just horror films, but comedies, dramas, and perhaps British period pieces.</p>

					<p>Now here she was in a chip commercial, the only work she'd had in three months.</p>

					<p>"Everybody back," the AD said.</p>

					<p>Ed returned, crawled under the table, reached up through a hole, and took control of the bag.</p>

					<p>A year ago, he'd been the monster that had eaten Alice Stall's character. Now here he was, a bag of chips. Half a bag.</p>

					<p>"Quiet on the set," the AD said.</p>

					<p>Twelve months earlier, she'd completed her senior project in film school. Since then, her resume had been thin. Assistant director on this commercial was the biggest thing she'd landed.</p>

					<p>The makeup lady took a quick look at Margery's face and nails and gave a thumb's up to the AD.</p>

					<p>The AD looked at the director, who repeated, "Quiet on the set."</p>

					<p>His last film was a prize-winning bio pic about Blind Lemon Jefferson. From that to this. In a year. Oh, well.</p>

					<p>"Action," he said.</p>

					<p>Margery eyed the half bag of chips and tapped her nails on the table. A closeup of a nail model's hand had been shot yesterday. All Margery had to do was a similar tapping at a similar place on the table.</p>

					<p>The chips ignored her, pretending to read the label on a nearby box of cookies.</p>

					<p>"Cut! Did someone move the cookies? The box looks closer to the edge. Too close to the edge."</p>

					<p>The AD measured the distance and moved the box to its proper mark.</p>

					<p>Three days to shoot this stupid commercial. She could have had it wrapped up in one. She looked at the director and gave a thumbs up.</p>

					<p>"When I say reset, I mean reset," the director said. "Come on, people. Let's get this done."</p>

					<p>Margery wished she'd gone to the bathroom when she had the chance. It would be terrible to repeat her performance of a year ago.</p>

					<p>During the space movie, she had peed her pants. Even though she knew the creature wasn't real, that it was Ed Markwood in a rubber suit with tracking sensors stuck all over it, Margery had been so frightened her bladder had let go. The production crew decided not to use the footage in the film, but had included it in the blooper reel on the DVD.</p>

					<p>"Action."</p>

					<p>Margery eyed the half bag of chips and tapped her nails on the table.</p>

					<p>The chips ignored her, pretending to read the label on a nearby box of cookies.</p>

					<p>"You know you want me," the bag of chips said, looking Margery's way.</p>

					<p>"No I don't," Margery replied.</p>

					<p>The bag of chips yawned and looked back at the cookie box.</p>

					<p>Margery reached out and grabbed the bag.</p>

					<p>"Gotcha!" she said.</p>

					<p>"Cut.</p>

					<p>"Chips, I'd like a slightly hurt expression before the yawn.</p>

					<p>"And Margery, wait a half beat longer before making the grab.</p>

					<p>"Reset."</p>

					<p>The box of cookies was not a real box of cookies, merely a prop. A year ago it had been the first officer on an exploratory mission that had discovered this miserable little planet with its miserable little life forms and their petty appetites.</p>

					<p>But for a dalliance with an engineering assistant, First Officer Cryxll would have been a captain by now with his own ship. But, no. Here he was. Sent down to impersonate a prop and do research on the psychology of human feeding rituals.</p>

					<p>"Action," the director said.</p>

					<p>The box of cookies couldn't help itself. Overcome by boredom and the sting of injustice, it fell over.</p>

					<p>"Cut!" the director yelled.</p>

					<p>He took off his headset and threw it on the floor. He stomped away three steps, then stomped back. He put his hands together in an attitude of prayer. "Please. Please. Would someone tape the cookies to the table."</p>

					<p>A gaffer came forward with a roll of tape.</p>

					<p>The director picked up his headset and put it on. "Where'd we get these props? You'd think they had a mind of their own," he muttered.</p>

					<p>Margery sighed.</p>

					<p>Ed sighed.</p>

					<p>The AD sighed.</p>

					<p>And though no one heard it, the box of cookies sighed, too.</p>

					<p>I'd better be careful, Cryxll thought. If I blow this, there are other research projects I could be assigned to. Mucus consumption rituals of the young came to mind, causing him to shutter.</p>

					<p>"Action," the director said, keeping an eye on the cookies.</p>
