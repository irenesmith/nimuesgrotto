					<h1>If Walls Could Talk</h1>
					<p class="author">by Elizabeth Sullivan</p>
					<p>She ran her hands across the peeling wallpaper. It was vibrant in its day, she was sure. Now it resembled a tattered cloth. As she came to one of the large windows on the enclosed porch, a piece of the wallpaper fell into her hand. She held it there, for just a moment, waiting. She felt it. That shimmer of excitement. She saw the vision of the wallpaper in the room, all fresh and new. </p>

					<p>As the vision faded she lifted her head to see the large door in front of her. Just a step up from the porch. She pushed the wooden door opened and heard the creak as she stepped into the living room. The sunlight filled the yellow room from the large picture window to her right. On the left was the staircase that led to the bedrooms upstairs. There was a calmness to this room. Something of a serene nature. She could sense that many family gatherings were had there. Many memories made. </p>

					<p>She could see a couch, large and comfortable, against the left wall, over looking the large window. There were children who played there. She walked to the picture window and saw the babbling creek about five feet from the house. It was so picturesque. Her heart told her that the people who lived there loved this home, loved the land it sat on. Walking through another doorway to her left she saw a bathroom, dining room and the kitchen. The kitchens back wall have been covered in bricks some time ago. As she studied the bricks she noticed the door to her right. </p>

					<p>Opening the door she felt herself gasp. Two large floor to ceiling windows brought complete natural light into the room. She felt her stomach flip as she looked at the bookshelves. They lined the right wall, and stood taller than she. Sight filled her as she saw a stoic desk placed just so in front of the windows. She could see a man spending hours at work at the desk, a woman curled up with a novel in the corner chair. </p>

					<p>There was a growing grin on her face as she left the library area and took a straight walk to the room off the kitchen. It seemed to be an addition to the home, not original like the rest of the house has been. She assumed it was for an extra bedroom. </p>

					<p>There was a door to the basement under the stairs to the upstairs. The steps creaked ever so lightly when she stepped on them. At the top of the stairs she felt the tears fill her eyes. She envisioned the master bedroom on one side and a nursery on the other. The view was so breathtaking, from both bedroom windows. </p>

					<p>"Hello?" she heard a female voice echo through the house. </p>

					<p>"Hello?" she asked, coming down the stairs. The brunette woman turned with a smile, her hand outstretched. </p>

					<p>"You must be Morgan. I'm Penny," she said as Morgan placed her hand in hers. The vision came so fast she nearly ripped her hand away. Morgan saw Penny, but this woman was older than the girl in front of her. She was standing in the very living room Morgan was in, smiling. As their hands fell, so did the vision. </p>

					<p>"Did you live here?" Morgan asked. </p>

					<p>"Yes. My brother and I grew up here. My parents decided to move down south, my brother is already down there, but I can't leave," she said turning slightly in the room. "This is my home, and I'm hoping you can help me decorate it?"</p>

					<p>Morgan felt the calmness again. It overwhelmed her as Penny explained how long the home had been in their family, how her fathers grandfather had built it and it traveled to the sons of the family. She was the first woman to take it over, and couldn't be more excited. So as Penny walked to the kitchen, Morgan laid a hand gently on the house, acknowledging that she got the message, she understood. This house was meant to be Penny's home, and Morgan was destined to help her with that. </p>
