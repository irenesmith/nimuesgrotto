					<h1>Things that go Bump in he Night</h1>
					<p class="author">by Irene P. Smith</p>

					<p>"It's all in my 'mag'nation," whispered Wayne.</p>

					<p>He pulled the blankets up under his chin. Then he held his breath and listened. Birds twittered their good nights to one another outside his window. Occasionally he heard a car pass by in the street below. Then as he continued to listen, breathing only when absolutely necessary, he heard the quiet sound of breathing. Somebody or something was standing in the corner of the room.</p>

					<p>"There's no such thing as ghosts," he said aloud.</p>

					<p>He listened again. This time he heard a rustling sound. It reminded him of the time his mother had dressed up in Gone with the Wind clothes for Halloween. It was the sound her skirts had made as she came in to kiss him before they left for the party. But Mommy and Daddy were asleep in their own room.</p>

					<p>Eyes wide, he stared into the darkness, straining his eyes to make out who was there.</p>

					<p>"Hello?"</p>

					<p>No answer. He dived under the blankets. Soon it was unbearably hot. His pajamas were soaked through as though he hadn't dried off after his bath. The air in the little cave formed by the blankets was stale and it was so dark he couldn't tell for sure whether his eyes were opened or closed. Just as he was thinking about coming out of hiding, the blankets were snatched from his body and thrown to the floor.</p>

					<p>His feet barely hit the ground as he ran down the hall to his parents' room. "Daddy," he cried as he shook his father's shoulder. "Daddy, wake up! There's a ghost in my room. It pulled the blankets off my bed."</p>

					<p>Sighing deeply his father sat up in bed and snapped on the light. He pulled Wayne onto his lap, kissed the top of his head, and then hugged him. "Listen, Buddy, didn't Mommy and I both tell you that there's no such thing as ghosts? It was probably just a bad dream, don't you think?"</p>

					<p>"Guess so," he muttered, hiding his face against his father's chest.</p>

					<p>Usually he liked it when Daddy called him Buddy, but this time it wasn't so great because he could tell that Daddy didn't really mean it. He was just trying to get rid of him and didn't believe what he said. The hair on Daddy's chest tickled his nose and made him sneeze. That woke up Mommy.</p>

					<p>"What's the matter, sweetheart?"</p>

					<p>"He says there's a ghost in his room," Daddy answered for him.</p>

					<p>Mommy's soft, cool hand touched his cheek. "Are you sure you're all right sweetie? You're burning up." Her hand slipped smoothly up the side of his face to his brow.</p>

					<p>"I was hiding under the blankets."</p>

					<p>"Still, I think you need a dose of baby aspirin." She got up and left the room as Daddy picked him up and carried him back to his own room.</p>

					<p>Daddy put him in bed and then turned on the lights. "See," he said as he opened the closet door, "there's nothing in here but your clothes, your shoes, and your toys."</p>

					<p>"Chew these up and then go back to sleep," Mommy said. She sat down next to him and waited as he chewed the bitter orange pills. "My goodness," she said, "what did you do to your blankets?"</p>

					<p>Working together, Mommy and Daddy picked up the blankets from the floor and tucked him in. "Remember, Sweetie, there's nothing here. If you hear something just remind yourself that it's the sound of the blood flowing through your body." Then Daddy turned off the light and they turned to leave. Daddy waved and Mommy blew him a kiss and they were both gone.</p>

					<p>He didn't believe them one bit. He knew there was a ghost in his house, in his room, but there was nothing he could do about it. He was afraid to get up again. He lay there on his back, every muscle tensed.</p>

					<p>Finally, after what seemed like hours, he began to fall asleep. Then he heard it. This time the sounds came through the open window. In seconds he was wide awake, staring through the darkness as best he could to try and make out what was there. This time there was no ghost. There was someone trying to come in the window.</p>

					<p>A man's head was silhouetted against the lighter rectangle of the window. The painters had left a ladder leaning against the back of the house and someone had decided to take advantage of it. He was afraid to scream, afraid to move, afraid to do anything but watch the burglar enter the house.</p>

					<p>Then he saw another shape come sailing out of the darkness, the semi-translucent shape of a woman in old-time clothing. Her skirts were wide and swung from side to side as she approached the window. She glowed with an eerie greenish-yellow light that was certainly visible to the burglar. Leaning forward, she gave the ladder a shove, sending it toppling backwards away from the window to crash loudly on the ground in the back yard. Then she turned towards Wayne, bowed slowly and he saw her smile as she slowly vanished.</p>

					<p>Things were exciting after that. Daddy called the police and they came to arrest the stunned burglar. Wayne didn't bother telling them about the ghost because he knew they wouldn't believe him but even though he never saw her again he always felt safe in his room at night whenever he heard her breathing.</p>
