					<h1>The Grand Illusion</h1>
					<p class="author">by Matthew D. Smith</p>
					<p>It was Halloween in Oakdale, and things were great. The year was 1978, and there was a huge festival in the city park. There was a small stage set up for all the performers that would be performing that night. Some of the performers there were groups like a new band named Van Halen, the more established Styx, and the coolest man alive, Nick Adams. Everyone was encouraged to come in costume, so I did just that. My costume was my favorite TV character, Mork from "Mork &amp; Mindy." My girlfriend Angie came with me as Mindy. As we walked around the park, we noticed other types of entertainment all over the place. The strangest one of all was a magician, calling himself Jackson the Great, standing on a little stage.</p>

					<p>Nick Adams was starting at around eight, and it was quarter to eight, so we figured we had enough time for a magic trick or two. As we watched the show, he pointed at Angie and me and said "Mork and Mindy, come up here."</p>

					<p>We walked up to the stage, where he gave Angie a purple bracelet.</p>

					<p>Once she had it on, he said "All right, now I'm going to make her disappear."</p>

					<p>He pulled out a silver hoop with a purple curtain attached. After he positioned the hoop over her head, he let the curtain drop, before saying "By the powers of the gods, you shall disappear."</p>

					<p>A huge puff of purple smoke went off, then he opened the curtain to reveal that Angie was gone.</p>

					<p>He closed the curtain, and said, "By the power of the gods, you shall reappear."</p>

					<p>Another puff of smoke went off and he opened the curtain. But Angie wasn't there. He repeated the process, but she still wasn't there. "Well folks, it looks like we've experienced some magical difficulty. My assistant will stand in for me while I fix the problem. Goodbye." He walked off the stage, heading towards the main stage. I followed him all the way backstage where I heard him say "No, no, no! This can't be right." He stared at the hoop, shaking it slightly, with a confused look on his face.</p>

					<p>I went up to him and said, "What did you do with my girlfriend?"</p>

					<p>"I'm not sure exactly," he said.</p>

					<p>"What! How are you not sure?"</p>

					<p>"Look, I don't have time to explain. All I can do right now is try to get her back." He pulled out a remote control and began fiddling around with it. Then he tried the trick again, and when he opened the curtain, Angie was there.</p>

					<p>She ran up to me, threw her arms around me, and held on tight. When I got a look at her face, I noticed she was as white as a ghost.</p>

					<p>"Angie, what's wrong?"</p>

					<p>"You wouldn't believe me if I told you," she said.</p>

					<p>"Babe, I'd believe anything you said."</p>

					<p>"I was in a van, and there were these men with huge deformed heads and red eyes. They spoke a language I've never heard before, and it looked like they were about to hurt me. Lucky for me, I was brought here before that happened."</p>

					<p>I was about to ask Jackson what she was talking about, but he was gone. We hurried out from backstage, so we wouldn't get in trouble, and joined the crowd for the Nick Adams concert.</p>

					<p>As we listened to Nick Adams sing the Bowie classic, "Queen Bitch," Jackson came up to me and said "There's a lot you don't know. All I can tell you is that the bracelet is what made her disappear and reappear. Some day, our paths will cross again, but until then, enjoy the rest of your life."</p>

					<p>Before I could answer, he was gone again. Angie and I enjoyed the concert, and went home to eat the candy we bought at the fair. We never saw Jackson again, but to this day, Angie and I wonder who those men were, and what Jackson had to do with them.</p>
