					<h1>Submission Guidelines</h1>
					<p>We are currently seeking submissions for the Winter, 2017 Issue of Nimue’s Grotto. The deadline for submissions to the Winter, 2017 issue is November 24, 2017.</p>

					<h2>Do you write flash fiction?</h2>

					<p>If so, we want to hear from you! We are looking for flash fiction in the genres of Fantasy, Science Fiction, and Horror. Stories of 1,000 words or less, please! There’s no lower limit. If you can tell a complete story in twenty-five words, go for it!</p>

					<p>What are we looking for?</p>

					<p>As I said above, we want Fantasy, Science Fiction, and Horror stories that are up to 1,000 words long.</p>

					<p>We want stories. In other words, complete stories with interesting characters, and stories that have a beginning, a middle, and an end.</p>

					<p>Entertain us. Make us laugh or cry, and best of all, make us think!</p>

					<p>We wish to purchase First Electronic Rights. This means that we wish to publish your story first on our website. After the next issue has been published, you are free to publish the story again anywhere you wish. We only ask that you credit Nimue’s Grotto as the first publisher. If you publish again online, we also ask that you include a link to the magazine’s main page at: http://nimuesgrotto.com.</p>

					<p>Unfortunately, we are not a paying market right now. We hope that will change sometime in the future, but for now all we can do is say, “Thank you!”</p>

					<h2>What do we NOT want?</h2>

					<p>We’re pretty flexible when it comes to fiction, but there are some things we are definitely notinterested in seeing:</p>

					<ul>
						<li>Genres other than those listed above.<br>

						In other words, we don’t want Contemporary Romance, Mystery, or Literary stories. Unless, of course, you can combine Romance and Fantasy or Mystery and Science Fiction, for example. Oh, and by “Fantasy” I mean magic, elves, and that kind of thing, not what you wish your spouse would do to you on a romantic get away. Which leads me to. . .<li>

						<li>Erotica<br>

						I love to read all kinds of stories, but we do not want stories that are mainly about sexual interactions, even between consenting adults.</li>

					<li>Non-fiction<br>

					We are not interested in non-fiction , movie reviews, book reviews, or interviews with the rich and famous. On the other hand, if you want to write non-fiction, and your topic is writing, the writing life, or self-publishing, consider submitting to “Dreams in Transit,” an online eZine for writers who write about writing. (Details on “Dreams in Transit” to follow.)</li>

					<li>Reprints<br>

					If you have published your story somewhere else, we don’t want it.</li>

					</ul>

					<h2>How to Submit</h2>

					<p>To submit a story for consideration, please paste it in the body of an email message, at the end of the story put the following:</p>

					<p>— The End —</p>

					<p>and follow it with one paragraph telling us a little bit about yourself. You can include links to your own website, or your publisher’s website.</p>

					<p>Make the subject something like this:</p>

					<p>SUBMISSION: story_title (e.g. SUBMISSION: Lost and Found)</p>

					<p>Send your submission to:</p>

					<p>editor@nimuesgrotto.com</p>
