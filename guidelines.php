          <h2>What are we looking for?</h2>

          <p>
            <strong><em>We've updated our guidlines!</em></strong> Starting
            with the Fall 2020 Issue, we are looking for book and movie reviews
            in addition to fiction.
          </p>

          <p>
            We are looking for flash fiction in the genres of Fantasy, Science
            Fiction, and Horror. Stories of 1,000 words or less, please!
            There’s no lower limit. If you can tell a complete story in
            twenty-five words, go for it!
          </p>

          <p>
            We want complete stories. In other words, stories with interesting
            characters, and stories that have a beginning, a middle, and an
            end.
          </p>

          <p>
            Entertain us. Make us laugh or cry, and best of all, make us think!
          </p>

          <p>
            We want First Electronic Rights. This means that we wish to publish
            your story first on our website. After six months, you are free to
            publish the story again anywhere you wish. We only ask that you
            credit Nimue’s Grotto as the first publisher. If you publish again
            online, we also ask that you include a link to the magazine’s main
            page at:
            <a ahref="https://nimuesgrotto.com">https://nimuesgrotto.com</a>.
          </p>

          <h3>Non-fiction</h3>

            <p>
              <strong>Something new!!</strong> Starting with Fall 2020, we are not
              accepting book and movie reviews but the genre of the book or movie
              must match the genres we publish. In other words, Horror, Fantasy,
              Science Fiction, or Fantasy.<br> Send us a query to
              <a href="mailto:editor@nimuesgrotto.com">editor@nimuesgrotto.com</a>,
              just in case we are already covering it.
            </p>

            <p>
              Unfortunately, we are not a paying market right yet. We hope that
              will change sometime in the future, but for now all we can do is
              say, “Thank you!” And publish your story, of course.
            </p>

            <h2>What we <strong>DO NOT</strong> Want</h2>

            <p>
              We’re pretty flexible when it comes to fiction, but there are some
              things we are definitely notinterested in seeing:
            </p>

            <ul>
              <li>Genres other than those listed above.<br>

                In other words, we don’t want Contemporary Romance, Mystery, or
                Literary stories. Unless, of course, you can combine Romance and
                Fantasy or Mystery and Science Fiction, for example. Oh, and by
                “Fantasy” I mean magic, elves, and that kind of thing, not what you
                wish your spouse would do to you on a romantic get away. Which
                leads me to. . .</li>

              <li>Erotica<br>

                I love to read all kinds of stories, but we do not want stories
                that are mainly about sexual interactions, even between consenting
                adults.</li>

              <li>Reprints<br>

                If you have published your story somewhere else, we don’t want it.</li>

            </ul>

