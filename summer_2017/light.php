					<h1>Light Always Accelerates</h1>
					<p class="author">by Ken Dushek</p>
						<p class="lead">“We don't warp the space-time boundaries to travel through a hole or vortex,” I explained. Her long orange hair covering the top half of her face. She pulls the majority of the blockade behind her ear, but it just splashes back into place a second later.</p>

						<p>
						“Then how do we travel?” she asks. Her innocence adorable but frustrating. In a few years, the school system will teach her the basics of space travel, and if my explanation disagrees with them—if she retains my answer all those years and builds up a false truth in her mind—if she argues in school a point make now without offering the whole picture—then I will have done her a great disservice.</p>

						<p>
						“The engines propel us in continuous acceleration,” I begin my response before another question interrupts. I pause to allow for the interruption to steer the conversation off topic and hopefully towards an answer I can justify to such a young mind.</p>

						<p>
						“What does that do? I think bending space would be cooler,” she innocently states, forgetting the question she led off with, unaware of the laws against bending the space-time vortex to travel at higher speeds.</p>

						<p>
						“Well, once we found out Einstein was lying about the speed of light being finite, all the science books were fixed to account for the acceleration of light instead,” I sense myself setting up an avalanche of arguments from her young mind, but she lacks the physics knowledge to understand the complexity of my explanation.</p>

						<p>
						“But if something accelerates, then it has to have speed, right?” she pushes the first snowball downhill surprisingly. A rather complex argument for a child who has yet to learn the science behind space travel.</p>

						<p>
						“Light never stops accelerating. Therefore it has no speed. That was the key to global destruction if Einstein revealed it at the time,” I desperately attempt to curtail some of the curiosities blooming on top of her head, but I promised myself I would never lie to her after sustaining such a heavy loss.</p>

						<p>
						“But we don’t travel by light, so what?” my sweet daughter rebelliously pushes off her corner of the breakfast table’s edge, demanding a logical answer.</p>

						<p>
						“Science went pretty haywire when the mad scientist saw his dream of global peace with endless energy supplies turned into weapons by mass murderers.”</p>

						<p>
						She’s silent. My opinion of Einstein may be too warped and dark for such a young mind.</p>

						<p>
						I worry I overstepped the PG rating our conversations are forced into to spare her beautiful innocence. Her hair now split along the sides of her face. She funnels the strands behind her ears and smiles as a spoonful of cereal makes its way almost into her mouth.</p>

						<p>
						“And?” she responds. I sense my last sentence was too much.</p>

						<p>
						“And now we know we are not bound by any limits, only those we force upon ourselves,” I attempt a conclusion of a happy nature in vain.</p>

						<p>
						“Space travel daddy!” she corrects.</p>

						<p>
						“And now we travel great distances, knowing we don’t age as long as we maintain a steep acceleration, imposing g-forces continuously to combat gravity’s wear on our souls, touring the farthest corners of heaven's reach without ever visiting the same planetary system twice.”</p>

						<p>
						“When’s mom coming home?” she asks. A question I could have dodged if I weren't so worried about keeping the conversation kid friendly. </p>

						<p>
						“Soon honey, soon.”</p>

						<p>
						“Is she meeting us at the next stop?” Her question forms a tear in my eye, but I pull it back, hoping to hide my melancholy. </p>

						<p>
						“I hope so,” barely escapes my lips.</p>

