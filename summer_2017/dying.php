					<h1>The Dying</h1>
					<p class="author">by Meredith Loughran</p>
					<p class="lead">I am the beginning and the end. No, I'm not talking about anything biblical but the catastrophic event of my birth is nothing short of all the bad parts in the Bible.</p>

					<p>By all accounts, I was born a normal child. My parents were as dysfunctional as the next. I had good friends. Then I hit puberty and everything changed; not just the regular hormonal bullshit and angst. I started having terrible nightmares and suddenly people around me were getting sick. Cancer. Pneumonia. Complications from... whatever.</p>

					<p>The pandemic started off slowly at first so there was no real cause for alarm when the dying began. It wasn't just the elderly, sickly, or terribly young. Weakened immune systems had nothing to do with it. Like watching dominoes fall at terminal velocity, people were dropping at a faster rate than the local funeral homes could handle. And when the funeral directors and all the doctors were gone, there wasn't anyone left to pick up the dead. Everybody was dying. That is -- everyone but me.</p>

					<p>Maybe it could have been contained to my one little town but state agencies came to investigate and inadvertently took the dying home which perpetuated the demise of the human race. It took the last-standing scientist to recognize the point of origin. You guessed it. Little ol' me.</p>

					<p>In this moment of clarity I swear I am not the cause but I've come to believe that I am the portal.  What ails the world manifested itself through me and I see the dying through my nightmares. I feel my strength grow as the dying feasts upon every village, town, and city; and my energy wanes as it waits for the unwitting soul to carry it elsewhere.</p>

					<p>This thing feeds me for reasons I cannot fathom. I've tried to take my own life, in fact, throwing myself off the steepest cliff to no avail for I can still hear the waves lapping at the rocky shore as my bones lie broken among the boulders. I still feel the warmth of the sun's rays upon my face. I see beautiful blue skies and witness evening stars twinkling merrily without the ambient light pollution from millions of homes. My skin has been pecked clean leaving my entrails open to the elements - but I am not dead. The dying wills me to be its living 
					ledger.</p>

					<p>The birds are circling again and I briefly wonder if the world is beautiful from that high above. Closing my eyes to dream I see there are no more people now. This time the birds are falling from the sky.</p>
