					<h1>Closure</h1>
					<p class="author">by John Governale</p>
					<p class="lead">After supper as Mrs. Cox was getting up to clear the table, Julie, instead of helping, stayed seated.</p>

					<p>"Mom," she said, "We need to talk."</p>

					<p>Mrs. Cox sat back down and looked at her daughter.</p>

					<p>"We need to talk about Dad,” Julie said. She watched as her mother’s gaze shifted to a snag in the tablecloth.</p>

					<p>"Did Dad commit suicide?"</p>

					<p>Mrs. Cox continued to stare at the table. Julie watched her mother's breathing and a small tic that flickered at the corner of the woman's mouth.</p>

					<p>"Mom?" Julie said, and then a bit more firmly, "Mom."</p>

					<p>Mrs. Cox looked at her daughter.</p>

					<p>"Did he commit suicide?"</p>

					<p>"No," Mrs. Cox said. "He didn't."</p>

					<p>"Then where did he go?"</p>

					<p>"He left us."</p>

					<p>"I know. But where did he go? I've searched the Internet from end to end and there's no trace of him."</p>

					<p>"If he's changed his name . . . " Mrs. Cox began, but was cut off by her daughter.</p>

					<p>"I would have found him. If he had started a new life somewhere else in the world, I would have found him. You know I would. Did he commit suicide?”</p>

					<p>The tic at the corner of Mrs. Cox’s mouth began jerking in twos and threes and her breathing changed.</p>

					<p>"No," Mrs. Cox said. "He did not."</p>

					<p>Silence settled on the room. The clock on the sideboard ticked. A car went by.</p>

					<p>"I'm sorry, Mom, but I have to ask you this. Did you kill him?"</p>

					<p>A shocked look lit Mrs. Cox’s face, her breathing slowed, and the tic at the corner of her mouth stopped.</p>

					<p>"Oh, Julie," she said.</p>

					<p>"Did you?"</p>

					<p>Mrs. Cox looked her daughter in the eyes."No, Julie. I did not kill your father."</p>

					<p>"Then where is he?"</p>

					<p>"I don't know. All I know is what his note said."</p>

					<p>The table was cleared and the dishwasher loaded in silence, then Julie and her mother went to their bedrooms and closed the doors.</p>

					<p>Julie removed a copy of Molecular Biology from her bookshelf and took it to her desk. Inside the worn dust jacket was not a science book, but Julie's journal. She sat down and began to write.</p>

					<p>"Mom is lying to me. I can tell. She believes—or she knows!—that Dad committed suicide. She'll never tell me why. She’ll never tell me where his body is.</p>

					<p>"I don't think she assisted him—she wasn't lying when she said she didn't kill him.</p>

					<p>"I guess I can stop searching. He’s dead, I’m sure, and I’m never going to see him again.”</p>

					<p>As Julie wrote, Mrs. Cox quietly locked her own bedroom door and removed a painting from the wall. Reaching out with her mind, she unlocked the safe, and the metal door swung open an inch. Mrs. Cox opened it the rest of the way, removed the xythmnth, sat cross-legged on the floor, and put the device on her head, a crystal over each temple.</p>

					<p>She reached out with her mind, past the moon, past the planets, past the trans-Neptunian objects. Past Alpha Centauri. Past Epsilon Indi. Past Gliese 832. She reached to the far side of the galaxy and touched a mind there.</p>

					<p>“My dearest Zanther. You were right; I was wrong. It would have been better to have faked your death than to pretend you had left us to start a new life with another woman. I couldn’t bear to have our daughter think you were dead, but now I know it would have been kinder.</p>

					<p>These past two years, your disappearance has haunted Julie, and she’s never stopped looking for you. She has extraordinary Internet skills and has scoured the net using everything from financial patterns to facial recognition.</p>

					<p>Tonight she asked me if you had committed suicide. I told her no, but used body language to convince her I was lying. It hurt me to do it, but now she is emotionally where she’d have been at 14 if we had followed your plan. Sorry.</p>

					<p>I am well, with no traces of the cellular breakdown that caused your return to Kaylia.</p>

					<p>Julie, too, is well. She recently had a physical for school sports. Other than low blood pressure and a slow heart rate—which is to be expected—there were no other indications that she is an earth-born Kaylian and not a human girl.</p>

					<p>Her eyesight and hearing are keen, but not so much as to startle anyone.</p>

					<p>Though we are a patient race, the days and weeks and years here seem long without you. I fear I am becoming an impatient earthling.</p>

					<p>When Julie marries and reproduces—which let us hope will not be too many years in the future—we’ll have a better idea if our species are compatible enough to warrant sending other couples to this planet.</p>

					<p>I love you and miss you and hope you have regained complete cellular stability.</p>

					<p>Your Thmanthnath.</p>

					<p>In her room, Julie returned Molecular Biology to the bookshelf, put on her pajamas, brushed her teeth, and got into bed. She flipped off her bedside lamp only to discover she’d left the overhead light on.</p>

					<p>She sighed and looked at the light switch next to the door, wishing she could do a Yoda and turn it off with her mind.</p>

					<p>The room went dark.</p>

					<p>Julie flipped on the bedside lamp and looked across the room at the light switch. It had been up, but was now in the down position.</p>

					<p>Mrs. Cox was hanging the painting over the safe when she felt the tug. Not sure what had happened, she froze for a second, then turned to look in the direction of Julie’s room. Though no human words could express her dismay, several choice ones slipped out before she lapsed into Kaylian.</p>
