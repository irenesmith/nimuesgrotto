					<h1>Albert's Plan</h1>
					<p class="author">by John Governale</p>
					<p>Albert Swan looked at his watch, then at the clock at the front of the classroom. According to both, 41 minutes remained.</p>

					<p>
					There were 80 questions total, and so far he’d answered 20. That’s about a question a minute, he thought. There were 60 questions left and only 41 minutes. To answer all 60 would require answering one every 40 seconds. Albert should have been worried, but he wasn’t. He had two things on his side.</p>

					<p>
					One, according to Professor Smith, no one answered all the questions. No one ever had. All Albert had to do was plow on and answer as many as he could, and no doubt, he would pass the exam and the class.</p>

					<p>
					Just passing, however, was not Albert’s plan. He was going to ace the test. Not only would he answer every question, he’d answer them all correctly.</p>

					<p>
					He smiled to himself and glanced at his watch. Only 40 minutes left. He looked at question 21.</p>

					<p>
					“Name four types of somatic cells found in cow’s milk.”</p>

					<p>
					Albert wrote lymphocytes, neutrophils, and eosinophils, but couldn’t think of a fourth.</p>

					<p>
					He made a small mark next to the question and moved on.</p>

					<p>
					He intended to answer as many questions as he could until there was one minute left. Then he would cheat. Albert again smiled to himself. The next question also required a small tick mark. Undismayed, he made the mark and continued.</p>

					<p>
					Normally, Albert was not a cheater. He didn’t need to be. Blessed with above average intelligence and good study habits, he had never failed a test. Ever. Not in primary school. Not in middle school. Not in high school. And, so far, not in college.</p>

					<p>
					The only reason he was going to cheat now was because of that twit, Cameron Howsley, whose grade point average matched Albert’s to three decimal places. No way on an exam graded on the curve was Howsley going to edge Albert by so much as a single question.</p>

					<p>
					It wasn’t really cheating if you were doing it to defeat a rival, to get a stronger A than he. There was no dishonor in looking up a few answers on the sly.</p>

					<p>
					“Thirty minutes left,” Professor Swan said, which elicited groans from several in the class.</p>

					<p>
					In spite of himself, Albert smiled again. Not counting the four questions he’d skipped, there were 37 left to answer. He increased his efforts, determined to knock out all 37 in 29 minutes.</p>

					<p>
					Time, as time will, marched on. Soon there were four minutes left. Albert wrote the answer to question 80 and put down his pen. Done!</p>

					<p>
					As others continued to scribble, Albert fingered the penny he’d put on his chair at the beginning of the test. He pressed it between the thumb and forefinger of his left hand and waited for the last minute to arrive so he could say in his mind the incantation that would stop time.</p>

					<p>
					When time stopped, Madame Furrioso had told him, he’d have as long as he wanted—years, even—to get the textbook and look up the answers.</p>

					<p>
					Not only was Albert going to look up the questions he’d skipped, he would double check the ones he’d answered.</p>

					<p>
					Three minutes. Two minutes. One minute.</p>

					<p>
					In his mind Albert said, “Coin of copper in my hand, this last minute I demand that time be placed at my command.”</p>

					<p>
					Just as Madame Furrioso had said, everything stopped, frozen in an instant. Albert looked over at Cameron Howsley sitting like a statue, a look of concern on his face as he was caught mid-glance in checking the clock.</p>

					<p>
					Albert had thought that potions, incantations, and such were fantasy—part of Harry Potter sorts of novels. But no. Madame Furrioso had just proven to him that magic was real.</p>

					<p>
					The clock was stuck at 10:59. Professor Smith was frozen looking at his pocket watch. Everybody was frozen, except Albert. Sound, itself, was fro. . . .</p>

					<p>
					From behind him, Albert heard pages being riffled through. He turned to see a young woman, not at all frozen, looking through her text book.</p>

					<p>
					“Who are you?” Albert asked.</p>

					<p>
					She started in surprise, looked up and said, “Who are you?”</p>

					<p>
					“Albert Swan. Who are you?”</p>

					<p>
					“Maggie Westerman. How come you’re not frozen?”</p>

					<p>
					Albert held up his left hand to show her the coin clamped between thumb and forefinger.</p>

					<p>
					Maggie got up, walked over, and held up her left hand, so he could see her thumb and finger pressing against a coin.</p>

					<p>
					The two of them laughed.</p>

					<p>
					“Madame Furrioso?” Maggie asked.</p>

					<p>
					Albert grinned and nodded.</p>

					<p>
					“How did you find out about her?” Maggie asked. “And how much did you pay?”</p>

					<p>
					“Believe it or not, my grandmother told me about . . .”</p>

					<p>
					Albert was not prepared for Maggie’s right hand. It darted out, fingers forward, and went for his armpit. It wasn’t a kung fu move. It wasn’t violent or aggressive at all. It was a classic armpit tickle.</p>

					<p>
					“Time’s up,” Professor Smith said. “Pen’s down.”</p>

					<p>
					Albert saw his coin lying on the floor several feet away.</p>

					<p>
					“Leave your answer sheets on your desks. Thank you all and good luck.”</p>

					<p>
					Albert turned and looked at Maggie Westerman, who smiled at him.</p>
