					<h1>Hidden</h1>
					<p class="author">by Elizabeth Sullivan</p>
					<p>He felt like he was out of his mind. There was no way that what she was saying was true.</p>

					<p>Jodie sat across from him, holding the picture in her hand. It was a sonogram, showing *his* child. That part he could believe. They had had a relationship. It hadn't lasted, but it *was* possible that she was carrying his child. What felt impossible was what she said next.</p>

					<p>"Liam, you have to stop shaking your head. This is true and you needed to know," Jodie said.</p>

					<p>Liam stood to pace, pacing made him feel better. "Jodie, you sound crazy."</p>

					<p>She stood up, face flushed with anger. "I thought you would understand," she yelled. She slapped the picture on the table. "Your daughter is probably going to be a mermaid, whether you think I'm crazy or not."</p>

					<p>He stopped pacing and looked at her, his face now filled with rage. He made an effort to speak calmly. "Daughter?" he asked. His voice was soft, softer than even he had expected.</p>

					<p>"Yes," she whispered. "Our doctors assured me that we will have a daughter. And they are a bit more accurate than yours."</p>

					<p>"Your doctors?" he asked.</p>

					<p>She bit her lip and sat back down. "Where I live, we—well we don't normally venture away from our neighborhood to meet—people like you," she said as Liam nodded. </p>

					<p>"So why did you venture out?" he asked. “And why did you stay? We dated for several months, after all.”</p>

					<p>She smiled before she answered, "I saw you in that coffee shop and my heart flew out of my chest."</p>

					<p>Her words surprised him. He remembered that day like it had just happened, not as if it was nearly two years ago. </p>

					<p>"So what now?" he asked. </p>

					<p>"Well, the council in my neighborhood doesn’t know yet. When they do they will demand that I marry the baby’s father. They won’t consider that the father could be a common—sorry—a man like you. They will think it is one of my people," she said.</p>

					<p>He examined the sonogram. It didn’t tell him anything.</p>

					<p>"Of course," she said, "there is still a chance that she will be human, and not a mermaid."</p>

					<p>"There is?" Liam asked. "What would that mean? Are we going to have to switch on and off, like co-parent?" he asked.</p>

					<p>Jodie's face got serious again. "I don't think you understand, Liam. If our daughter is a mermaid, you will never see her. She will have to stay with me. We can't risk that kind of exposure. She would never be able to attend your schools, and she wouldn’t even dare to be caught out in the rain."</p>

					<p>"What if she's human?" he asked.</p>

					<p>Jodie lowered her eyes to her protruding belly. She lay her hand flat against it and closed her eyes.</p>

					<p>"If she's human, it’s safest that you keep her. I will be sure that she doesn't know about me." she said.</p>

					<p>Liam shook his head. "No, there must be something other way, Jo. I'm just getting my footing in this world. Let me come with you. Let me live where you do. I can help you raise her—human or mermaid—it’s a job we should share."</p>

					<p>The words sounded crazy coming from his mouth, even to his own ears, but his heart couldn't stop them.</p>

					<p>"It can't happen like that. I will come back before she's born. My doctor will come with me and I will have her here and we can go from there," she said as she stood and walked away.</p>

					<p>"Wait!"</p>

					<p>"How is it I never noticed, you know?" he asked looking at her legs.</p>

					<p>"It only happens when we get wet. Don’t you know the stories? We stay very well hidden," she said softly. By the time he got to the door, she had disappeared into the night.</p>

					<p>#</p>

					<p>As promised, Jodie returned, a few months later. She was accompanied by her doctor and an older gentlemen. She was very pregnant and seemingly in pain.</p>

					<p>While the doctor and the older man readied the birthing area, Jodie spoke quietly with Liam. “Birth is different for us mermaids,” she said. “Thanks to our doctors advanced screenings, we are well-prepared for the birth and it’s usually pretty fast. Normally an hour or less.</p>

					<p>When the doctor returned, he examined her and then lead her into the other room. True to her words, an hour later the doctor lead Liam to Jodie’s side. They both watched as he examined the baby. He hook his head as he held up their daughter. Liam looked to Jodie for explanation.</p>

					<p>A single tear rolled, unheeded, down her cheek. "She's human, Liam," she whispered.</p>

					<p>She handed the baby to him and turned away. He looked down into the trusting eyes that looked back into his. As he felt his own tears start to fall, he hugged the infant tightly.</p>

					<p>"What should we name her?" he asked.</p>

					<p>Jodie shook her head. "She's yours," she said.</p>

					<p>The doctor lead him out of the room and ushered in his companion. Half an hour later, he heard the two men leave the guest room and head for the living room. Liam’s heart was heavy. He had just become used to the idea of having a family and now they would be leaving him forever. </p>

					<p>"You take care of them,” he said. “They're precious."</p>

					<p>Them? Liam watched the two men leave. Clutching their daughter he walked through the doors to where Jodie sat, sobbing as though her heart was broken. </p>

					<p>"My father let me go," she whispered.</p>

					<p>Liam grinned as he looked at their daughter. </p>

					<p>"Sophia," she whispered holding her arms out for their daughter. "My mothers name is Sophia."</p>

					<p>Later that night, as he watched Jodie feed Sophia, he smiled, grateful that he would have them both in his life for good.</p>
