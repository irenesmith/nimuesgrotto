					<h1>Anomaly</h1>
					<p class="author">by Boris Antonovich</p>
					<p class="lead">Heavy snow was falling in Moscow. Galina and Natalya watched the familiar sight through a window.</p>

					<p>"No two snowflakes are alike," Natalya said. </p>

					<p>"What do you mean?"</p>

					<p>"Just what I said. No two snowflakes are alike." </p>

					<p>"That's silly," Galina said. "Many snowflakes are alike."</p>

					<p>"You're wrong. Science has proven it. Every snowflake is different."</p>

					<p>Galina gave her sister a look she often used when Natalya said preposterous things.</p>

					<p>Natalya gave Galina a look she often used in response.</p>

					<p>"You're saying that out of the millions—billions—of snowflakes falling right now, no two are alike? If we looked at one flake and then at all the rest, there wouldn't be another like it—they'd all be different?"</p>

					<p>Natalya nodded. "That's what I'm saying."</p>

					<p>At that moment, more than 4,000 miles away, two sisters were having the same conversation as they looked through their living room window at a Maine sky that was unloading two feet of the white stuff.</p>

					<p>"But that's just silly,"Mary said. "There are too many snowflakes for it to be true. Too many. There has to be some that are the same."</p>

					<p>Nancy shook her head. "None," she said. "And I'll tell you something else. If there ever were two snowflakes alike . . . ."</p>

					<p>". . . if there ever were two snowflakes alike," Natalya said, "it would create an anomaly in the space/time continuum and the universe would implode."</p>

					<p>Galina hated it when her sister talked like this, the same way that Mary hated it when Nancy did.</p>

					<p>"You've been watching too much Star Trek," Galina said.</p>

					<p>"You've been watching too much Star Trek," Mary said. "If there were two snowflakes—one here and one, say, in Russia—if there were two exactly alike, I'm sure . . . "</p>

					<p>". . . I'm sure the space/time continuum would be rugged enough to handle it." </p>

					<p>The chances that a snowflake in Maine and a snowflake in Russia would be identical were astronomical. But the chances that twin sisters in Maine and twin sisters in Moscow would be having identical conversations when those two flakes fell was more than the space/time continuum could bear.</p>

					<p>"So what happened?" Xrthinal asked.</p>

					<p>"There was a big bang," Uwinth replied. "The entire framework of space and time lost it's structure and condensed down to a single point, which was followed by a tremendous explosion."</p>

					<p>Xrthinal shook his caripace and rolled all of his eyes.</p>

					<p>"So you are saying that because of a coincidence, the universe reset itself? Your absurdness overwhelms me."</p>

					<p>Uwinth clicked his pincers in irritation. "Yes. The universe reset itself."</p>

					<p>"If the universe reset itself, how do you even know that story?"</p>

					<p>"I know things," Uwinth said.</p>

					<p>"So if two sentient beings on a planet many light years from here happened to have the exact same conversation we are having, it would effect the entire universe, causing it to implode?"</p>

					<p>"Yes," Uwinth said between gritted mandibles. "Space and time couldn't handle a coincidence of that magnitude."</p>

					<p>Xrthinal had had enough of this conversation. "If such a coincidence were to happen, I'm sure space and time would be resilient enough to survive it," he said and laughed.</p>

					<p>It was the last laugh for many billions of years. </p>
